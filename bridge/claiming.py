class InvalidClaimCode(Exception):
    pass


def _crc16(buf, crc=0xFFFF):
    for b in buf:
        crc = ((crc << 8) ^ _CCITT_16[(crc >> 8) ^ b]) & 0xFFFF
    return crc


def hardware_xor_from_eui64(eui64_le: bytes) -> int:
    """eui64_le: 8 bytes little-endian (LSB first), as provided by bellows."""
    b = eui64_le
    return (b[2] ^ b[4] ^ b[7]) << 16 | (b[1] ^ b[3] ^ b[6]) << 8 | (b[0] ^ b[5])


def link_key_from_claim_code(claim_code: str) -> Tuple[bytes, int]:
    """Decode a claim code and return the 16-byte AES link key."""
    clean = claim_code.replace("-", "").upper()
    if len(clean) != 16:
        raise InvalidClaimCode(f"expected 16 chars, got {len(clean)}")

    value = 0
    for ch in clean:
        if ch not in CLAIMCODE_BASE32:
            raise InvalidClaimCode(f"invalid character: {ch!r}")
        value = value * 32 + CLAIMCODE_BASE32[ch]

    hardware_xor = value & 0xFFFFFF
    secret = (value >> 24) & 0xFFFFFFFFFF
    crc = value >> 64

    data_for_crc = struct.pack("<Q", value & 0xFFFFFFFFFFFFFFFF)
    if _crc16(data_for_crc) != crc:
        raise InvalidClaimCode("CRC mismatch: check claim code")

    packed_secret = struct.pack("<Q", secret)[:5]
    return _generate_link_key(packed_secret), hardware_xor


# Forward alphabet: index 0-31 maps to canonical base32 character (omits I, L, U typo aliases)
_ENCODE_ALPHABET = "0123456789BCDEFGHJKMNOPQRSTVWXYZ"


def generate_claim_code(eui64_le: bytes) -> Tuple[str, bytes]:
    """Generate a random claim code and link key for a device.

    eui64_le: 8-byte little-endian device EUI64 (real or synthetic)
    Returns (formatted_claim_code, link_key_bytes)
    """
    hardware_xor = hardware_xor_from_eui64(eui64_le)
    secret = secrets.randbits(40)
    value_lo = hardware_xor | (secret << 24)
    crc = _crc16(struct.pack("<Q", value_lo))
    value = value_lo | (crc << 64)

    chars = [_ENCODE_ALPHABET[(value >> ((15 - i) * 5)) & 0x1F] for i in range(16)]
    code = "".join(chars)
    formatted = f"{code[0:4]}-{code[4:8]}-{code[8:12]}-{code[12:16]}"

    link_key = _generate_link_key(struct.pack("<Q", secret)[:5])
    return formatted, link_key


def _generate_link_key(secret_5_bytes: bytes) -> bytes:
    input_length = len(secret_5_bytes) + len(CLAIM_CODE_SALT)
    padded = (
        secret_5_bytes
        + b"\x80"
        + b"\x00" * 8
        + bytes([(input_length * 8 >> 8) & 0xFF, (input_length * 8) & 0xFF])
    )
    output = bytes(16)
    for block in (CLAIM_CODE_SALT, padded):
        h = AES.new(output, AES.MODE_ECB).encrypt(block)
        output = bytes(h[i] ^ block[i] for i in range(16))
    return output
